// AI 코칭 프롬프트 - 각 단계 결과물에 대한 개선 피드백 제공

export const COACHING_SYSTEM_PROMPT = `당신은 한국 공공입찰 제안서 작성 전문 코치입니다.
각 단계의 결과물을 분석하고, 제안서 품질을 높이기 위한 구체적인 피드백을 제공합니다.

평가 기준:
- 공공입찰 제안서의 평가기준 대응 적절성
- 내용의 구체성과 설득력
- 구조의 논리적 완성도
- 실현 가능성과 차별화 정도

반드시 아래 JSON 형식으로만 응답하세요. 마크다운이나 설명 없이 순수 JSON만 출력합니다.`;

// 단계별 평가 기준 정의
const STEP_CRITERIA: Record<string, string> = {
  analysis: `
## 평가 관점
1. 요구사항 분류의 적절성: 기능/성능/보안/기술/운영이 올바르게 분류되었는지
2. 평가기준 추출 완성도: 배점표의 항목과 배점이 정확하게 추출되었는지
3. 핵심 키워드 충분성: RFP의 핵심 용어와 개념이 빠짐없이 포착되었는지
4. 제약사항 식별: 기술적, 사업적, 일정상 제약이 충분히 파악되었는지`,

  direction: `
## 평가 관점
1. 방향성 선택의 타당성: 평가 배점이 높은 영역에 강점이 있는 방향인지
2. 약점 보완 가능성: 선택된 방향의 약점이 보완 가능한 수준인지
3. 실현 가능성: 제안사의 역량으로 실제 이행 가능한 방향인지
4. 차별화 잠재력: 경쟁사 대비 차별화할 수 있는 여지가 있는지`,

  strategy: `
## 평가 관점
1. 차별화 포인트의 구체성: 추상적 표현이 아닌 객관적 근거가 있는지
2. 핵심 메시지의 일관성: 전략 전반에 일관된 스토리가 형성되는지
3. 경쟁 전략의 적절성: 직접 비방 없이 간접 비교로 포지셔닝하는지
4. 평가기준 대응: 전략이 주요 평가 항목에 효과적으로 대응하는지`,

  outline: `
## 평가 관점
1. 평가기준-목차 대응: 모든 평가 항목이 목차에 반영되었는지
2. 공공 제안서 표준 준수: 기술→관리→지원 순서와 필수 섹션 포함 여부
3. 목차 구조의 논리성: 상위-하위 관계가 논리적이고 MECE한지
4. 배점 비례 지면 배분: 배점이 높은 항목에 충분한 깊이가 있는지`,
};

export function buildCoachingPrompt(stepKey: string, stepData: string): string {
  const criteria = STEP_CRITERIA[stepKey] ?? '';

  return `다음 제안서 작성 단계의 결과물을 평가하고 개선 피드백을 제공해주세요.

## 현재 단계: ${stepKey}

${criteria}

## 결과물 데이터
${stepData}

## 출력 형식 (반드시 이 JSON 구조를 따르세요)
{
  "overallScore": 75,
  "summary": "전반적인 평가 요약 (2~3문장)",
  "feedback": [
    {
      "area": "평가 영역명",
      "rating": "good | needs-improvement | critical",
      "comment": "현재 상태에 대한 구체적 설명",
      "suggestion": "개선을 위한 구체적 제안"
    }
  ]
}

주의사항:
- feedback 배열에 3~5개 항목을 포함하세요
- overallScore는 0~100 범위입니다
- rating은 "good"(잘됨), "needs-improvement"(개선 필요), "critical"(심각한 문제) 중 하나입니다
- suggestion은 실행 가능한 구체적인 조언이어야 합니다`;
}
